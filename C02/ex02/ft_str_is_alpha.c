int		is_alpha(char c)
{
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
		return (1);
	return (0);
}

int		ft_str_is_alpha(char *str)
{
	int i;

	i = 0;
	while (str[i] && is_alpha(str[i]))
		i++;
	if (str[i] == '\0')
		return (1);
	return (0);
}
