int		ft_is_prime(int nb)
{
	unsigned int i;

	i = 3;
	if (nb < 0)
		return (0);
	if (nb == 0 || nb == 1)
		return (0);
	if ((nb % 2 == 0 && nb != 2) || nb % 3 == 0)
		return (0);
	while (i * i <= (unsigned int)nb)
	{
		if (nb % i == 0)
			return (0);
		i += 2;
	}
	return (1);
}
